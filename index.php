    <small>The Alaska Highway (also known as the Alaskan Highway, Alaska-Canadian Highway, or ALCAN Highway) was constructed during World War II for the purpose of connecting the contiguous United States to Alaska across Canada. It begins at the junction with several Canadian highways in Dawson Creek, British Columbia, and runs to Delta Junction, Alaska, via Whitehorse, Yukon. Completed in 1942 at a length of approximately 1,700 miles (2,700 km), as of 2012 it is 1,387 mi (2,232 km) long. The difference in distance is due to constant reconstruction of the highway, which has rerouted and straightened out numerous sections. The highway was opened to the public in 1948.[1] Legendary over many decades for being a rough, challenging drive, the highway is now paved over its entire length.

    An informal system of historic mileposts developed over the years to denote major stopping points; Delta Junction, at the end of the highway, makes reference to its location at "Historic Milepost 1422." It is at this point that the Alaska Highway meets the Richardson Highway, which continues 96 mi (155 km) to the city of Fairbanks. This is often regarded, though unofficially, as the northern portion of the Alaska Highway, with Fairbanks at Historic Milepost 1520.[2] Mileposts on this stretch of highway are measured from Valdez, rather than the Alaska Highway. The Alaska Highway is popularly (but unofficially) considered part of the Pan-American Highway, which extends south (despite its discontinuity in Panama) to Argentina.
